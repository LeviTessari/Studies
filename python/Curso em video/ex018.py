import math

ang = float(input('Digite o angulo: '))

math.radians(30)

rad = ang * math.pi/180

print('O angulo escolhido é {}\nO valor do seu seno é {:.2f}\nO valor do seu cosseno é {:.2f}\nO valor da sua tangente é {:.2f}'.format(math.radians(30), math.sin(math.radians(30)), math.cos(rad), math.tan(rad)))